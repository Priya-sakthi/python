# Program to perform different set operations
# as we do in  mathematics

# sets are define
A = {2,5,6,8,9,-9}
B = {4,7,3,8,2,1}

# union
print("Union :", A | B)

# intersection
print("Intersection :", A & B)

# difference
print("Difference :", A - B)

# symmetric difference
print("Symmetric difference :", A ^ B)